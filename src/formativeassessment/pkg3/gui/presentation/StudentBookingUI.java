    private Resource<StudentBooking> getStudentBooking() {
        if(nameTF.getText().isBlank()){ 
            return Resource.failure("Some field is blank");
        }if(surnameTF.getText().isBlank()){
            return Resource.failure("Some field is blank");
        }if(emailTF.getText().isBlank()){
            return Resource.failure("Some field is blank");
        }if(phoneTF.getText().isBlank()){
            return Resource.failure("Some field is blank");
        }if(emailTF.getText().length() > 15){
            return Resource.failure("Email too long");
        }if(nameTF.getText().length() > 10){
            return Resource.failure("Name too long"); 
        }if(surnameTF.getText().length() > 10){
            return Resource.failure("Surname too long");
        }if(phoneTF.getText().length() > 10){
            return Resource.failure("Phone number too long");
        }
        return Resource.success(
            new StudentBooking(
                nameTF.getText(),
                surnameTF.getText(),
                emailTF.getText(),
                phoneTF.getText(),
                ((Subject)subjectComboBox.getSelectedItem()).name()
            )
        );
    }
    
    private void showErrorMessage(String message) {
        JOptionPane.showMessageDialog(rootPane, message, null, JOptionPane.ERROR_MESSAGE);
    }
    
    private void showInfoMessage(String message) {
        JOptionPane.showMessageDialog(rootPane, message);
    }
