    private void insertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertBtnActionPerformed
        // TODO add your handling code here:
        Resource<StudentBooking> booking = getStudentBooking();
        if(booking.isSuccess()){
            Resource<StudentBooking> result = repository.insertStudentBooking(booking.getSuccess().getResult());
            if(result.isFailure()){
                showErrorMessage(result.getFailure().getReason());
            }else{
                showInfoMessage(booking.getSuccess().getResult().fName() + " has been added to the list");
            }
        }else{
            showInfoMessage(booking.getFailure().getReason());
        }
    }//GEN-LAST:event_insertBtnActionPerformed

    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:
        Resource<List<StudentBooking>> bookings = repository.getAllSudentBooking();
        if(bookings.isFailure()){
            showErrorMessage(bookings.getFailure().getReason());
            return;
        }
        DefaultTableModel model = (DefaultTableModel) bookingTable.getModel();
        //clear table
        for(int index = 0 ; index < model.getRowCount() ; index++){
            model.removeRow(index);
        }
        
        for(StudentBooking booking : bookings.getSuccess().getResult()){
            model.addRow(new Object[]{booking.fName(), booking.lName(), booking.email(), booking.phone(), booking.subject()});
        }
        model.fireTableDataChanged();
        scrollPane.revalidate();
        scrollPane.repaint();
    }//GEN-LAST:event_viewBtnActionPerformed


